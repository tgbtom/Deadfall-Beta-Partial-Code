                <h3><u>Understanding the Pages in Town</u></h3>
                <h4>Homepage/Bulletin Board</h4>
                <p class="info">The homepage is the page that contains a log of the important events that have occurred in the town. (Currently only logs when new characters join the town).
                    This page can be accessed by clicking on the <em>Deadfall Banner</em> at the top of the page.</p>
                
                <h4>Construction Tab</h4>
                <p class="info">The construction page is where you can view, and contribute to all available construction sites. Structures will only appear on this page when your town meets the requirements.
                When your town is holding enough required resources for a construction, you will have the option to contribute AP towards it's completion. After as little as 1 AP has been invested into a new construction, all required resources will be removed from the bank, and dedicated
                to that structure.</p>
                
                <h4>Storage Tab</h4>
                <p class="info">The storage tab is where you can view the community inventory for the town. Each town begins with a specific supply of food and water to ensure you can get through a handful of days. Any resources that you wish to put towards a construction must be deposited 
                into the town storage by navigating to this page and clicking <em>'drop'</em> on the item. Conversely, items may be retreived from storage simply by clicking on the item while on this page. If your character is carrying too much, the item will remain in storage.</p>
                
                <h4>Citizens Tab</h4>
                <p class="info">The citizens tab is a quick overview of all citizens that are in the town. This page is helpful because it allows a user to switch between their characters simply by clicking on the characters name on this page.
                This page also displays important statuses of each citizen, such as; if they are dead, and if they have finished their actions for the day.</p>
                
                <h4>Statistics Tab (Ending a Day)</h4>
                <p class="info">The statistics page is under construction, and will be used more as the game develops. Currently this page is where you will go to end the current day, on the character you are logged into. You want to navigate to this page and press the end day button on each character
                 when they have completed all of the actions you wish for them to do for the day. Once you press 'End Day' for the last of your citizens in the town, the day will flip over and the horde will attack. The horde size is displayed along the top, beside the towns overall defense. For the first 5 days in a town, the horde will remain the size it is when the town is created (or shrink depending on how many zombies you kill).
                After day 5, when you end the day, zombies will begin spreading on the map, thus increasing the horde size.</p>
		
                <h4>Outside Tab</h4>
                <p class="info">The outside tab is arguably the place that you will spend most of your time in the game. This tab is where you will navigate your survivors to scavenge the map outside of the town, and fight off the horde before it attacks each night.
                 Each zed that you kill outside of the town will reduce the horde size by 1, and reduce the spread of zombies accordingly (based on proximity). Killing zombies in the early days of a town is going to be essential in the long run, as it delays the inevitable, making it take longer for the horde to grow to an uncontrollable size. 
                Each particular zone can only be properly looted from 10 times, then the zone will become depleted, significantly reducing your chances of obtaining any worthwhile loot. </p>
